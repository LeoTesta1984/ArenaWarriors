package warriorsarena;

/**
 *
 * @author Casa
 */
public class Point {
    private int coorX;
    private int coorY;

    public Point(int coorX, int coorY) {
        this.coorX = coorX;
        this.coorY = coorY;
    }

    public int getCoorX() {
        return coorX;
    }

    public void setCoorX(int coorX) {
        this.coorX = coorX;
    }

    public int getCoorY() {
        return coorY;
    }

    public void setCoorY(int coorY) {
        this.coorY = coorY;
    }
    
    
}
